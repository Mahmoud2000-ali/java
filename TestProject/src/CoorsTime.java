class CoorsTime {
  private String day, hour;
 public CoorsTime(String day, String hour) {
 this.day = day;
 this.hour = hour;
 }
 public CoorsTime(String day) {
 this.day = day;
 }
 /*
 setters methods
 */
 public void setDay(String day) {
 this.day = day;
 }
 public void setHour(String hour) {
 this.hour = hour;
 }
 /*
 getters methods
 */
 public String getDay() {
 return this.day;
 }
 public String getHour() {
 return this.hour;
 }
 } 
    
